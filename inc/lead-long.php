		<p>Roger Mooking has developed a culinary philosophy built on perfect execution of globally inspired culinary traditions. As executive chef he has been the subject of numerous television and print features internationally and nationally, including Food Network, HGTV, The New York Times, Wallpaper magazine and Toronto Life. A third-generation restaurateur and chef, he began his formal training through the George Brown Culinary Management Program, where he earned a Top Student Honours diploma. Roger continued his training at Epic Dining Room in Toronto's world-renowned Royal York Hotel before consulting on several food and beverage openings.</p>
		<p>Roger is currently executive chef/co-owner of Kultura Social Dining and Nyood Restaurant in downtown Toronto, where he has earned a reputation as one of the city's premier chefs.</p>
		<p>He is the host and co-creator of his Cooking Channel series entitled Everyday Exotic. Also, he is the recording artist and creator of Soul Food, a unique food and music project released by Warner Music.</p>
		<p>For Roger, music and the epicurean world are seamless. One feeds the body, the other the soul; it is all food in all its various forms. </p>

